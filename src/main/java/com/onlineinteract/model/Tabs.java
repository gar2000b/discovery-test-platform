package com.onlineinteract.model;

/**
 * Tabs Data Model making up the workspace state.
 * 
 * @author Gary Black
 *
 */
public class Tabs {
    String name;
    String tab;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTab() {
        return tab;
    }

    public void setTab(String tab) {
        this.tab = tab;
    }
}
